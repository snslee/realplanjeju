-- ============================================================================
-- SECTION 6. RPC — rpc_list_catalog (사업부별 활성 카탈로그)
-- ============================================================================

CREATE OR REPLACE FUNCTION rpc_list_catalog(
  _사업부 VARCHAR
)
RETURNS TABLE(
  id UUID,
  카테고리 VARCHAR,
  품명 VARCHAR,
  내용 TEXT,
  기본_단가 NUMERIC,
  기본_단위 VARCHAR,
  display_order INT
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
#variable_conflict use_column
BEGIN
  IF NOT fn_current_is_admin() THEN
    RAISE EXCEPTION '관리자만 카탈로그를 조회할 수 있습니다';
  END IF;

  RETURN QUERY
    SELECT
      pc.id, pc.카테고리, pc.품명, pc.내용,
      pc.기본_단가, pc.기본_단위, pc.display_order
    FROM product_catalog pc
    WHERE pc.사업부 = _사업부
      AND pc.is_active = TRUE
    ORDER BY pc.카테고리, pc.display_order, pc.품명;
END;
$$;

GRANT EXECUTE ON FUNCTION rpc_list_catalog(VARCHAR)
  TO anon, authenticated;

