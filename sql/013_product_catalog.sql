-- ============================================================================
-- SECTION 1. product_catalog 테이블 DDL
-- ============================================================================

CREATE TABLE IF NOT EXISTS product_catalog (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  사업부 VARCHAR(20) NOT NULL,
  카테고리 VARCHAR(50) NOT NULL,
  품명 VARCHAR(100) NOT NULL,
  내용 TEXT,
  기본_단가 NUMERIC(12,2),
  기본_단위 VARCHAR(20),
  display_order INT DEFAULT 0,
  is_active BOOLEAN DEFAULT TRUE,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now()
);

ALTER TABLE product_catalog DROP CONSTRAINT IF EXISTS ck_catalog_사업부;
ALTER TABLE product_catalog ADD CONSTRAINT ck_catalog_사업부
  CHECK (사업부 IN ('국내여행','행사이벤트','온라인마케팅','마케팅교육'));

-- 동일 사업부 내 동일 품명 방지 (UNIQUE)
ALTER TABLE product_catalog DROP CONSTRAINT IF EXISTS uq_catalog_사업부_품명;
ALTER TABLE product_catalog ADD CONSTRAINT uq_catalog_사업부_품명
  UNIQUE (사업부, 카테고리, 품명);

-- ============================================================================
-- SECTION 2. 인덱스 2종
-- ============================================================================

CREATE INDEX IF NOT EXISTS idx_catalog_사업부_order
  ON product_catalog(사업부, 카테고리, display_order);
CREATE INDEX IF NOT EXISTS idx_catalog_active
  ON product_catalog(사업부, is_active) WHERE is_active = TRUE;

-- ============================================================================
-- SECTION 3. updated_at 트리거
-- ============================================================================

DROP TRIGGER IF EXISTS trg_catalog_updated ON product_catalog;
CREATE TRIGGER trg_catalog_updated BEFORE UPDATE ON product_catalog
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- ============================================================================
-- SECTION 4. RLS 정책 2종
-- ============================================================================

ALTER TABLE product_catalog ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS admin_all_catalog ON product_catalog;
CREATE POLICY admin_all_catalog ON product_catalog
  FOR ALL TO anon, authenticated
  USING (fn_current_is_admin())
  WITH CHECK (fn_current_is_admin());

DROP POLICY IF EXISTS auth_select_active_catalog ON product_catalog;
CREATE POLICY auth_select_active_catalog ON product_catalog
  FOR SELECT TO authenticated
  USING (is_active = TRUE);

